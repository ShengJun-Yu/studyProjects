package demo04ArrayList;

import java.util.ArrayList;

/**
 * @author : Bojack
 * @date : 2021年10月13日20:54:52
 * 如果希望向集合ArrayList当中存储基本类型数据，必须使用基本类型对应的“包装类”。
 *
 * 基本类型    包装类（引用类型，包装类都位于java.lang包下）
 * byte        Byte
 * short       Short
 * int         Integer     【特殊】
 * long        Long
 * float       Float
 * double      Double
 * char        Character   【特殊】
 * boolean     Boolean
 *
 * 从JDK 1.5+开始，支持自动装箱、自动拆箱。
 *
 * 自动装箱：基本类型 --> 包装类型
 * 自动拆箱：包装类型 --> 基本类型
 */
public class ArrayListBasic {
    public static void main(String[] args) {
        ArrayList<Integer> num =new ArrayList<>();
        num.add(10);
        num.add(13);
        num.add(12);
        System.out.println("数组列表为：" + num);
        int size=num.size();
        System.out.println("数组列表长度为" + size);

    }
}
